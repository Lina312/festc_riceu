      FUNCTION AUNIF(IX)
      ! THIS SUBPROGRAM GENERATES UNIFORM RANDOM NUMBERS.
      K=IX/127773
      IX=16807*(IX-K*127773)-K*2836
      IF(IX<0)IX=IX+2147483647
      AUNIF=IX*4.656612875D-10
      RETURN
      END
